package operators;

public class AssignmentOperator {
    public static void main(String[] args) {
        int a=9;
        a+=3;// a = a + 3 → a = 12
        a *= 2;  // a = a * 2 → a = 24
        a -=4; //a = a - 4 → a = 20
        a /=2; // a = a / 2 → a = 10
        System.out.println(a);
    }
}
